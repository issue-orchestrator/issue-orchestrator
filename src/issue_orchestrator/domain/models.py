@dataclass
class Issue:
    """A GitHub issue.

    Note: This class is being deprecated in favor of the Issue Protocol
    and GitHubIssue implementation. New code should use ports.issue.Issue
    and adapters.github.GitHubIssue.
    """
    number: int
    title: str
    labels: list[str]
    state: str = "open"  # "open" or "closed"
    repo: str = ""  # Repository in owner/repo format
    milestone: Optional[str] = None
    body: Optional[str] = None
    milestone_number: Optional[int] = None
    milestone_due_on: Optional[str] = None  # ISO date string

    @property
    def key(self) -> IssueKey:
        """Stable identity for this issue.

        Uses external ID from title prefix (e.g., [M1-011]) if present,
        otherwise falls back to the issue number as a string.
        """
        parsed = parse_external_id(self.title)
        external_id = parsed.external_id or str(self.number)
        return GitHubIssueKey(repo=self.repo, external_id=external_id)

    @property
    def agent_type(self) -> Optional[str]:
        """Extract agent type from labels (e.g., 'agent:web')."""
        for label in self.labels:
            if label.startswith("agent:"):
                return label
        return None

    @property
    def priority(self) -> int:
        """Extract priority (lower = higher priority)."""
        if "priority:high" in self.labels:
            return 1
        elif "priority:medium" in self.labels:
            return 2
        elif "priority:low" in self.labels:
            return 3
        return 4

    @property
    def is_blocked(self) -> bool:
        """Check if issue has any blocking label.

        Blocking labels: blocked, blocked-*, needs-human, failed (legacy).

        Blocked is a MODIFIER label that coexists with in-progress.
        An issue can be both in-progress AND blocked - this means:
        - We maintain ownership/claim on the issue
        - Work is paused waiting for intervention
        - When blocking label is removed, work resumes immediately
        """
        return any(_is_blocking_label(l) for l in self.labels)

    @property
    def is_in_progress(self) -> bool:
        """Check if issue has the 'in-progress' label."""
        return any(_base_of(l) == "in-progress" for l in self.labels)

    @property
    def needs_human(self) -> bool:
        """Check if issue specifically needs human intervention.

        Subset of blocked - checks for: needs-human, blocked-needs-human.
        """
        return any(_base_of(l) in ("blocked-needs-human", "needs-human") for l in self.labels)


@dataclass
class AgentConfig:
    """Configuration for an agent type."""
    prompt_path: Path
    # Original relative path from config - used in commands so agents read from worktree
    prompt_relative: str = ""
    # Provider name (e.g., "claude-code", "codex") - validated against agent_runner registry
    # If not set, uses default_agent.provider. If that's also not set, config validation fails.
    provider: Optional[str] = None
    model: str = "sonnet"
    timeout_minutes: int = 45
    # Provider-specific arguments (e.g., permission_mode for claude-code, approval_mode for codex)
    provider_args: dict[str, Any] = field(default_factory=dict)
    # Permission mode for Claude CLI: default, acceptEdits, bypassPermissions, plan, dontAsk
    # Deprecated: use provider_args instead. Kept for backwards compatibility.
    permission_mode: str = "default"
    # Skip code review for this agent (e.g., domain-expert agents that don't produce code)
    skip_review: bool = False
    # Per-agent reviewer override (uses review.default if not set)
    reviewer: Optional[str] = None
    # Command template - escape hatch to override provider-generated command
    # If set, this is used instead of provider.build_command()
    # For non-interactive mode, add -p flag (Claude), use 'exec' (Codex), or -p (Gemini)
    # Note: {system_prompt} includes agent-done instructions + "Read {prompt} for task instructions"
    command: str = "claude {claude_args} --permission-mode {permission_mode} --model {model} --append-system-prompt '{system_prompt}' '{initial_prompt}'"
    # Optional override for hook verification AI agent (e.g., "claude-code")
    meta_agent: Optional[str] = None
    initial_prompt: str = "Work on issue #{issue_number}: {issue_title}. Follow the instructions in {prompt}. When done, exit with /exit."
    # AI system type for log retrieval (e.g., "claude-code", "codex", "gemini", "aider")
    # If not set, auto-detected from command. Must match an entry in ai-systems.yaml.
    ai_system: Optional[str] = None
    # Path to retry prompt template (relative to repo root). Used when validation fails.
    # If not set, uses validation.retry_prompt_template or built-in default.
    # Template variables: {original_task}, {validation_cmd}, {error_file}, {error_summary},
    #                     {retry_count}, {max_retries}
    retry_prompt_template: Optional[str] = None

    def get_command(
        self,
        issue_number: int,
        issue_title: str,
        worktree: Path,
        pr_number: Optional[int] = None,
        existing_work: Optional[str] = None,
    ) -> str:
        """Render the command template with actual values, including initial prompt.

        If a provider is configured, uses the provider's build_command() method.
        Otherwise, falls back to the legacy command template.

        Args:
            issue_number: The GitHub issue number
            issue_title: The issue title
            worktree: Path to the worktree
            pr_number: Optional PR number (for review agents). If a prompt template
                       uses {pr_number} but this is not provided, a KeyError is raised.
            existing_work: Optional context about existing commits in the worktree.
                          If provided, prepended to prompt so agent knows to complete vs restart.
        """
        # Build format kwargs - pr_number only included if provided (fail-fast if used but missing)
        # Use relative prompt path so agents read from worktree (not main repo)
        prompt_for_command = self.prompt_relative if self.prompt_relative else str(self.prompt_path)
        format_kwargs = {
            "issue_number": issue_number,
            "issue_title": issue_title,
            "prompt": prompt_for_command,
            "worktree": worktree,
            "model": self.model,
            "permission_mode": self.permission_mode,
            "claude_args": os.environ.get("ORCHESTRATOR_CLAUDE_ARGS", "").strip(),
        }
        if pr_number is not None:
            format_kwargs["pr_number"] = pr_number

        # First render the initial prompt
        rendered_prompt = self.initial_prompt.format(**format_kwargs)

        # Prepend existing work context if provided
        if existing_work:
            rendered_prompt = f"IMPORTANT: {existing_work}\n\n{rendered_prompt}"

        # If provider is set, use provider-based command building
        if self.provider:
            return self._build_provider_command(rendered_prompt, prompt_for_command)

        # Legacy template-based command building
        from issue_orchestrator.resources import get_agent_done_instructions

        # Escape single quotes in the prompt for shell safety
        escaped_prompt = rendered_prompt.replace("'", "'\\''")

        # Build the full system prompt with agent-done instructions
        agent_done_docs = get_agent_done_instructions()
        system_prompt = (
            f"{agent_done_docs}\n\n"
            f"---\n\n"
            f"Read {prompt_for_command} for your task-specific instructions."
        )
        # Escape single quotes for shell embedding
        escaped_system_prompt = system_prompt.replace("'", "'\\''")

        # Then render the full command with the prompt included
        format_kwargs["initial_prompt"] = escaped_prompt
        format_kwargs["system_prompt"] = escaped_system_prompt

        prompt_mode = os.environ.get("ORCHESTRATOR_CLAUDE_PROMPT_MODE", "arg").strip().lower()
        if prompt_mode == "stdin":
            template = (
                "printf '%s' '{initial_prompt}' | "
                "claude {claude_args} --input-format text "
                "--permission-mode {permission_mode} --model {model} "
                "--append-system-prompt '{system_prompt}'"
            )
            return template.format(**format_kwargs)

        return self.command.format(**format_kwargs)

    def _build_provider_command(self, prompt: str, prompt_file: str) -> str:
        """Build command using the configured provider.

        Args:
            prompt: The fully rendered prompt to send to the agent
            prompt_file: Path to the prompt/instructions file (for system prompt)

        Returns:
            Shell-safe command string
        """
        from issue_orchestrator.agent_runner import get_provider
        from issue_orchestrator.resources import get_agent_done_instructions

        # self.provider is guaranteed non-None here (caller checks)
        assert self.provider is not None
        provider = get_provider(self.provider)

        # Build provider-specific kwargs from provider_args
        kwargs = dict(self.provider_args)

        # STRICT ENFORCEMENT: Agent-done is ALWAYS injected for ALL providers.
        # - For Claude: inject via system_prompt (--append-system-prompt)
        # - For other providers: prepend to the prompt itself
        agent_done_docs = get_agent_done_instructions()
        agent_done_with_prompt_ref = (
            f"{agent_done_docs}\n\n"
            f"---\n\n"
            f"Read {prompt_file} for your task-specific instructions."
        )

        if self.provider == "claude-code":
            # Claude Code uses --append-system-prompt for agent-done
            system_prompt = agent_done_with_prompt_ref
            # Append any user-provided system_prompt (agent-done always comes first)
            user_system_prompt = kwargs.pop("system_prompt", None)
            if user_system_prompt:
                system_prompt = f"{system_prompt}\n\n---\n\n{user_system_prompt}"
            kwargs["system_prompt"] = system_prompt
            # Use permission_mode from provider_args or fall back to legacy field
            kwargs.setdefault("permission_mode", self.permission_mode)
        else:
            # Other providers (Codex, etc.): prepend agent-done to the prompt itself
            prompt = f"{agent_done_with_prompt_ref}\n\n---\n\n{prompt}"

        # Build the command (returns list[str])
        cmd_list = provider.build_command(prompt=prompt, model=self.model, **kwargs)

        # Convert to shell-safe string
        return shlex.join(cmd_list)


@dataclass
class Session:
    """A running Claude session working on an issue.

    The `key` field is the domain identity (SessionKey) used for:
    - Duplicate prevention at launch
    - Lookup in active sessions
    - Removal when done
    - State machine lookup

    The `terminal_id` is an opaque handle that only the terminal adapter interprets.
    """
    key: SessionKey  # Domain identity - (IssueKey, TaskKind)
    issue: "IssueProtocol"
    agent_config: AgentConfig
    terminal_id: str  # Opaque handle - only terminal adapter interprets this
    worktree_path: Path
    branch_name: str
    completion_path: str = COMPLETION_RECORD_PATH  # Agent-specific path to completion.json
    agent_label: Optional[str] = None  # Agent type label (e.g., "agent:backend") for per-agent reviewer
    pr_number: int | None = None  # PR number for review/rework sessions
    rework_cycle: int | None = None  # Which rework iteration (1, 2, ...) — None for initial coding
    started_at: datetime = field(default_factory=datetime.now)
    status: SessionStatus = SessionStatus.RUNNING
    exit_sent: bool = False  # Track if we've already sent /exit
    validation_retry_count: int = 0  # Number of validation retries attempted
    original_prompt: str | None = None  # Original prompt for retry context
    last_validation_error: str | None = None  # Last validation error for retry prompt
    last_validation_error_file: str | None = None  # Path to validation error file
    last_output_monotonic: float | None = None
    last_output_at: float | None = None
    last_log_mtime: float | None = None
    last_log_size: int | None = None
    last_output_tail: str | None = None
    last_no_output_monotonic: float | None = None
    # Claim/lease fields for multi-orchestrator coordination
    lease_id: str | None = None  # Unique identifier for the claim
    lease_acquired_at: datetime | None = None  # When the claim was acquired
    lease_expires_at: datetime | None = None  # When the claim expires if not renewed
    last_claim_verified_at: datetime | None = None  # Last time we verified we're still the winner

    @property
    def runtime_minutes(self) -> int:
        """How long this session has been running."""
        delta = datetime.now() - self.started_at
        return int(delta.total_seconds() / 60)

    @property
    def is_timed_out(self) -> bool:
        """Check if session exceeded timeout."""
        return self.runtime_minutes > self.agent_config.timeout_minutes


