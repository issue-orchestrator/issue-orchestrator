    awaiting_merge_checks_pending_since: dict[int, float] = field(default_factory=dict)
    # Repo-wide backoff state for status-check-rollup reads. Bounds repeated
    # `status_check_rollup` permission failures so a token that cannot read
    # check status does not re-trigger the same GraphQL probe (and the same
    # warning) on every tick. See StatusRollupGate.
    status_rollup_capability: StatusRollupCapability = field(
        default_factory=StatusRollupCapability
    )
    # Candidate queue removals waiting for a targeted confirmation refresh.
    queue_pending_shrink_missing_issue_numbers: list[int] = field(default_factory=list)
    queue_pending_shrink_confirm_at: float = 0.0
    queue_pending_shrink_prior_count: int = 0  # Queue size before the pending shrink
    queue_pending_shrink_candidate_count: int = 0  # Queue size proposed by the pending shrink
    issue_last_refreshed_at: dict[int, float] = field(default_factory=dict)  # issue_number -> epoch seconds
    # Main-loop heartbeat — populated by run_tick. These let the view model
    # distinguish "refresh is old because GitHub hasn't been polled" from
    # "refresh is old because the main loop is stuck". A monotonically
    # increasing last_tick_completed_at is the authoritative signal that
    # the orchestrator is alive; a never-completed last_tick_started_at
    # alongside a stale completion time points to a phase that is blocking.
    last_tick_started_at: float = 0.0  # Epoch seconds; 0 if no tick has started
    last_tick_completed_at: float = 0.0  # Epoch seconds; 0 if no tick has completed
    current_tick_phase: str = ""  # Non-empty only while a tick is mid-flight
