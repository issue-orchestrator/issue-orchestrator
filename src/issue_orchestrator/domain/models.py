def _base_of(label: str) -> str:
    """Strip an optional single colon-delimited prefix, returning the base name."""
    # "bot:in-progress" → "in-progress", "in-progress" → "in-progress"
    # "blocked:claim-lost" has *semantic* colons so we only strip if the
    # first segment looks like a prefix (no dash).
    first, sep, rest = label.partition(":")
    if sep and "-" not in first and rest:
        return rest
    return label


def _is_blocking_label(label: str) -> bool:
    """Pure string check for blocking labels — handles both prefixed and unprefixed."""
    base = _base_of(label)
    if (
        # _base_of("blocked:claim-lost") returns "claim-lost", so keep the
        # unprefixed semantic-colon form before checking prefix-stripped bases.
        label.startswith("blocked:")
        or base == "blocked"
        or base.startswith("blocked-")
        or base.startswith("blocked:")
    ):
        return True
    return base in ("needs-human", "failed", "publish-failed")

# Path for completion records relative to worktree root
# Each session writes to a distinct file based on agent name to avoid
# race conditions when multiple sessions reuse the same worktree
COMPLETION_RECORD_PATH = ".issue-orchestrator/completion.json"  # Legacy fallback
COMPLETION_DIR = ".issue-orchestrator"


def get_completion_path(
    agent_name: str | None = None,
    session_name: str | None = None,
    run_dir: str | None = None,
) -> str:
    """Get the completion file path for an agent.

    Args:
        agent_name: The agent label (e.g., "agent:e2e-test"). If None, uses legacy path.
        session_name: Session name for per-session output directories.

    Returns:
        Path like ".issue-orchestrator/completion-agent_e2e-test.json"
    """
    if run_dir:
        completion_dir = f"{COMPLETION_DIR}/sessions/{run_dir}"
        if not agent_name:
            return f"{completion_dir}/completion.json"
        safe_name = sanitize_agent_label(agent_name)
        return f"{completion_dir}/completion-{safe_name}.json"
    if session_name:
        completion_dir = f"{COMPLETION_DIR}/sessions/{session_name}"
        if not agent_name:
            return f"{completion_dir}/completion.json"
        # Sanitize agent name for filename (replace : and other special chars)
        safe_name = sanitize_agent_label(agent_name)
        return f"{completion_dir}/completion-{safe_name}.json"
    if not agent_name:
        return COMPLETION_RECORD_PATH
    # Sanitize agent name for filename (replace : and other special chars)
    safe_name = sanitize_agent_label(agent_name)
    return f"{COMPLETION_DIR}/completion-{safe_name}.json"


def sanitize_agent_label(agent_name: str) -> str:
    return agent_name.replace(":", "_").replace("/", "_").replace(" ", "_")


