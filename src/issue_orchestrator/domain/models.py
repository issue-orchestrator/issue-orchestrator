@dataclass
class AgentConfig:
    """Configuration for an agent type."""
    prompt_path: Path
    # Original relative path from config - used in commands so agents read from worktree
    prompt_relative: str = ""
    # Provider name (e.g., "claude-code", "codex") - validated against agent_runner registry
    # If not set, uses default_agent.provider. If that's also not set, config validation fails.
    provider: Optional[str] = None
    model: str = "sonnet"
    timeout_minutes: int = 45
    # Provider-specific arguments (e.g., permission_mode for claude-code, approval_mode for codex)
    # Claude permission modes: default, acceptEdits, bypassPermissions, plan, dontAsk
    provider_args: dict[str, Any] = field(default_factory=dict)
    # Opt-in (default OFF) to the orchestrator-computed OS sandbox (ADR-0034),
    # under a TRUSTED-repository contract: the operator onboards the target repo,
    # so its Claude config + the operator's user/managed settings are trusted, and
    # the sandbox constrains the AGENT. When True, a claude-code launch replaces
    # yolo ``bypassPermissions`` with ``dontAsk`` + a bounded ``--settings``:
    # Bash and native file-tool WRITES are worktree-scoped, known secrets and
    # restricted egress are denied, and the agent cannot modify its own policy
    # files. Non-secret native READS outside the worktree remain possible by
    # design (this is not a read jail). Codex uses an invocation-scoped native
    # permission profile for equivalent write/secret/egress boundaries; its
    # additional read roots are also writable because Codex has no read-only
    # ``--add-dir``. Linked-worktree commits receive narrow access to the current
    # worktree's Git state/current ref and the shared loose-object store; other
    # shared Git metadata stays read-only. Provider-less/custom-command agents
    # are rejected.
    # Distinct from codex's ``provider_args.sandbox`` (a codex CLI policy string).
    sandbox: bool = False
    # Skip code review for this agent (e.g., domain-expert agents that don't produce code)
    skip_review: bool = False
    # Per-agent reviewer override (uses review.default if not set)
    reviewer: Optional[str] = None
    # Command template - escape hatch to override provider-generated command
    # If set, this is used instead of provider.build_command()
    # For one-shot mode, add -p flag (Claude), use 'exec' (Codex), or -p (Gemini)
    # Note: {system_prompt} includes completion instructions + "Read {prompt} for task instructions"
    command: str = "claude -p {claude_args} --permission-mode {permission_mode} --model {model} --append-system-prompt '{system_prompt}' '{initial_prompt}'"
    # Optional override for hook verification AI agent (e.g., "claude-code")
    meta_agent: Optional[str] = None
    initial_prompt: str = "Work on issue #{issue_number}: {issue_title}. Follow the instructions in {prompt}. When done, use coding-done to report completion."
    # AI system type for log retrieval (e.g., "claude-code", "codex", "gemini", "aider")
    # If not set, auto-detected from command. Must match an entry in ai-systems.yaml.
    ai_system: Optional[str] = None
    # Path to retry prompt template (relative to repo root). Used when validation fails.
    # If not set, uses validation.retry_prompt_template or built-in default.
    # Template variables: {original_task}, {validation_cmd}, {error_file}, {error_summary},
    #                     {retry_count}, {max_retries}
    retry_prompt_template: Optional[str] = None

    @property
    def effective_permission_mode(self) -> str:
        """Claude permission mode from ``provider_args`` (``"default"`` when unset).

        Single read shared by command rendering, session identity metadata,
        and failure diagnostics — there is exactly one place permission mode
        is spelled (``provider_args.permission_mode``) and one place it is
        resolved.
        """
        mode = self.provider_args.get("permission_mode")
        if isinstance(mode, str) and mode:
            return mode
        return "default"

    def _initial_prompt_template(self, task_kind: str) -> str:
        """Initial prompt template for ``task_kind``.

        An explicitly configured ``initial_prompt`` always wins. When the
        field still holds its coding-flavored dataclass default, review-kind
        launches get the review default instead — a reviewer must never be
        told to do coding work and call coding-done.
        """
        field_default = type(self).__dataclass_fields__["initial_prompt"].default
        if self.initial_prompt != field_default:
            return self.initial_prompt
        if task_kind in _REVIEW_TASK_KINDS:
            return DEFAULT_REVIEW_INITIAL_PROMPT
        return self.initial_prompt

    def render_initial_prompt(
        self,
        issue_number: int,
        issue_title: str,
        worktree: Path,
        pr_number: Optional[int] = None,
        existing_work: Optional[str] = None,
        task_kind: str = TaskKind.CODE.value,
    ) -> str:
        """Render the session prompt text before command wrapping."""
        prompt_for_command = self.prompt_relative if self.prompt_relative else str(self.prompt_path)
        format_kwargs = {
            "issue_number": issue_number,
            "issue_title": issue_title,
            "prompt": prompt_for_command,
            "worktree": worktree,
            "model": self.model,
            "permission_mode": self.effective_permission_mode,
            "claude_args": os.environ.get("ORCHESTRATOR_CLAUDE_ARGS", "").strip(),
        }
        if pr_number is not None:
            format_kwargs["pr_number"] = pr_number

        rendered_prompt = self._initial_prompt_template(task_kind).format(**format_kwargs)
        if existing_work:
            rendered_prompt = f"IMPORTANT: {existing_work}\n\n{rendered_prompt}"
        return rendered_prompt

    def resolve_launch_provider(self) -> Optional[str]:
        """Provider identity for building a LAUNCH command.

        ``provider`` wins. An explicitly configured ``command`` (anything
        other than the field default) is an intentional override and keeps
        template-based launching — exchange stub agents configure a custom
        ``command`` alongside a real ``ai_system``. Otherwise ``ai_system``
        resolves: it shares the provider-registry vocabulary
        (``claude-code``, ``codex``).

        Exists because session CLASSIFICATION (``provider or ai_system``,
        see ``persistent_session_exchange._agent_provider``) and command
        BUILDING (``provider`` only, else the legacy ``claude -p`` template)
        resolved differently: an ``ai_system="codex"`` reviewer with no
        ``provider`` was classified as codex but silently launched as
        print-mode claude, which renders nothing and hangs the round to its
        full timeout (caught by the real-codex exchange smoke test).
        """
        if self.provider:
            return self.provider
        if self.command != type(self).__dataclass_fields__["command"].default:
            return None
        return self.ai_system

    def get_command(
        self,
        issue_number: int,
        issue_title: str,
        worktree: Path,
        pr_number: Optional[int] = None,
        existing_work: Optional[str] = None,
        task_kind: str = "code",
        extra_provider_args: Optional[dict[str, Any]] = None,
    ) -> str:
        """Render the command template with actual values, including initial prompt.

        If a provider is configured, uses the provider's build_command() method.
        Otherwise, falls back to the custom command template.

        Args:
            issue_number: The GitHub issue number
            issue_title: The issue title
            worktree: Path to the worktree
            pr_number: Optional PR number (for review agents). If a prompt template
                       uses {pr_number} but this is not provided, a KeyError is raised.
            existing_work: Optional context about existing commits in the worktree.
                          If provided, prepended to prompt so agent knows to complete vs restart.
            task_kind: The task kind value (e.g., "code", "rework", "review", "tech_lead").
                      Determines which completion instructions are injected.
        """
        prompt_for_command = self.prompt_relative if self.prompt_relative else str(self.prompt_path)
        rendered_prompt = self.render_initial_prompt(
            issue_number=issue_number,
            issue_title=issue_title,
            worktree=worktree,
            pr_number=pr_number,
            existing_work=existing_work,
            task_kind=task_kind,
        )
        return self.get_command_for_prompt(
            rendered_prompt,
            issue_number=issue_number,
            issue_title=issue_title,
            worktree=worktree,
            pr_number=pr_number,
            prompt_file=prompt_for_command,
            task_kind=task_kind,
            extra_provider_args=extra_provider_args,
        )

    def get_command_for_prompt(
        self,
        rendered_prompt: str,
        *,
        worktree: Path,
        issue_number: int = 0,
        issue_title: str = "",
        pr_number: Optional[int] = None,
        prompt_file: str | None = None,
        task_kind: str = "code",
        evidence_read_roots: tuple[Path, ...] = (),
        extra_provider_args: Optional[dict[str, Any]] = None,
    ) -> str:
        """Build an agent command for an already-rendered prompt.

        ``evidence_read_roots`` is the read-only god-view grant for a tech-lead
        failure investigation (empty otherwise): the sandbox reads those roots
        while writes stay confined to ``worktree`` (#6824 R5).
        """
        prompt_for_command = prompt_file or (
            self.prompt_relative if self.prompt_relative else str(self.prompt_path)
        )
        # Resolve the per-session sandbox scope (ADR-0034) BEFORE choosing the
        # provider vs legacy-template path. Returns None unless this agent opted
        # in. ``sandbox: true`` is a security opt-in, so if a scope is requested
        # and there is no enforcing provider adapter (the provider-less/custom-
        # ``command`` launch mode), FAIL LOUD rather than silently render an
        # unsandboxed legacy-template command.
        sandbox_scope = compute_session_scope(
            self,
            SandboxScopeContext(
                task_kind=task_kind,
                worktree=worktree,
                evidence_read_roots=evidence_read_roots,
            ),
        )
        if sandbox_scope is not None and not self.provider:
            raise SandboxUnsupportedError(
                "sandbox: true is not supported for a provider-less / custom-command "
                "agent — the launcher cannot enforce the sandbox. Use a provider "
                "adapter (provider: claude-code), or set sandbox: false."
            )
        if self.provider:
            # The worktree anchors the sandbox's read/write roots; the task kind
            # selects the role. Each provider adapter must enforce or fail loud.
            return self._build_provider_command(
                rendered_prompt,
                prompt_for_command,
                task_kind,
                extra_provider_args,
                sandbox_scope=sandbox_scope,
            )

        # Legacy template-based command building (never sandboxed — guarded above)
        from issue_orchestrator.resources import get_completion_instructions

        # Escape single quotes in the prompt for shell safety
        escaped_prompt = rendered_prompt.replace("'", "'\\''")

        # Build the full system prompt with completion instructions
        completion_docs = get_completion_instructions(task_kind)
        system_prompt = (
            f"{completion_docs}\n\n"
            f"---\n\n"
            f"Read {prompt_for_command} for your task-specific instructions."
        )
        # Escape single quotes for shell embedding
        escaped_system_prompt = system_prompt.replace("'", "'\\''")

        # Then render the full command with the prompt included
        format_kwargs = {
            "issue_number": issue_number,
            "issue_title": issue_title,
            "prompt": prompt_for_command,
            "worktree": worktree,
            "model": self.model,
            "permission_mode": self.effective_permission_mode,
            "claude_args": os.environ.get("ORCHESTRATOR_CLAUDE_ARGS", "").strip(),
            "initial_prompt": escaped_prompt,
            "system_prompt": escaped_system_prompt,
        }
        if pr_number is not None:
            format_kwargs["pr_number"] = pr_number

        prompt_mode = os.environ.get("ORCHESTRATOR_CLAUDE_PROMPT_MODE", "arg").strip().lower()
        if prompt_mode == "stdin":
            template = (
                "printf '%s' '{initial_prompt}' | "
                "claude {claude_args} --input-format text "
                "--permission-mode {permission_mode} --model {model} "
                "--append-system-prompt '{system_prompt}'"
            )
            return template.format(**format_kwargs)

        return self.command.format(**format_kwargs)

    def _build_provider_command(
        self,
        prompt: str,
        prompt_file: str,
        task_kind: str,
        extra_provider_args: dict[str, Any] | None = None,
        *,
        sandbox_scope: SandboxScope | None = None,
    ) -> str:
        """Build command using the configured provider.

        Args:
            prompt: The fully rendered prompt to send to the agent
            prompt_file: Path to the prompt/instructions file (for system prompt)
            task_kind: The task kind value (e.g., "code", "rework", "review", "tech_lead").
            extra_provider_args: Per-issue overrides (e.g., from labels) merged on top
                of the agent's ``provider_args``.
            sandbox_scope: The per-session sandbox scope (ADR-0034), or ``None``
                when the agent has not opted in. ``None`` yields the exact
                unsandboxed command as before.

        Returns:
            Shell-safe command string
        """
        from issue_orchestrator.agent_runner import get_provider
        from issue_orchestrator.resources import get_completion_instructions

        # self.provider is guaranteed non-None here (caller checks)
        assert self.provider is not None
        provider = get_provider(self.provider)

        # Build provider-specific kwargs from provider_args, with per-issue overrides
        kwargs = dict(self.provider_args)
        if extra_provider_args:
            kwargs.update(extra_provider_args)

        # STRICT ENFORCEMENT: Completion instructions are ALWAYS injected for ALL providers.
        # - For Claude: inject via system_prompt (--append-system-prompt)
        # - For other providers: prepend to the prompt itself
        completion_docs = get_completion_instructions(task_kind)
        completion_with_prompt_ref = (
            f"{completion_docs}\n\n"
            f"---\n\n"
            f"Read {prompt_file} for your task-specific instructions."
        )

        if self.provider == "claude-code":
            # Claude Code uses --append-system-prompt for completion instructions
            system_prompt = completion_with_prompt_ref
            # Append any user-provided system_prompt (completion instructions always come first)
            user_system_prompt = kwargs.pop("system_prompt", None)
            if user_system_prompt:
                system_prompt = f"{system_prompt}\n\n---\n\n{user_system_prompt}"
            kwargs["system_prompt"] = system_prompt
            # setdefault keeps per-issue extra_provider_args overrides
            # authoritative over the agent's effective_permission_mode.
            kwargs.setdefault("permission_mode", self.effective_permission_mode)
        else:
            # Other providers (Codex, etc.): prepend completion instructions to prompt
            prompt = f"{completion_with_prompt_ref}\n\n---\n\n{prompt}"

        # Build the command (returns list[str])
        model: Optional[str] = self.model
        if (
            model == type(self).__dataclass_fields__["model"].default
            and self.provider != "claude-code"
        ):
            # The field default ("sonnet") is claude vocabulary. Forwarding
            # it to another provider sends e.g. ``codex --model sonnet``,
            # which the codex backend rejects (400) and the TUI then idles
            # for the whole round timeout — caught live by the real-codex
            # exchange smoke test. An untouched default is "no explicit
            # choice": let the provider use its own default model.
            model = None
        cmd_list = provider.build_command(
            prompt=prompt, model=model, sandbox_scope=sandbox_scope, **kwargs
        )

        # Convert to shell-safe string
        return shlex.join(cmd_list)


@dataclass
class Session:
    """A running Claude session working on an issue.

    The `key` field is the domain identity (SessionKey) used for:
    - Duplicate prevention at launch
    - Lookup in active sessions
    - Removal when done
    - State machine lookup

    The `terminal_id` is an opaque handle that only the terminal adapter interprets.
    """
    key: SessionKey  # Domain identity - (IssueKey, TaskKind)
    issue: "IssueProtocol"
    agent_config: AgentConfig
    terminal_id: str  # Opaque handle - only terminal adapter interprets this
    worktree_path: Path
    branch_name: str
    run_assets: SessionRunAssets  # Exact session artifacts allocated before launch.
    completion_path: str = COMPLETION_RECORD_PATH  # Agent-specific path to completion.json
    agent_label: Optional[str] = None  # Agent type label (e.g., "agent:backend") for per-agent reviewer
    pr_number: int | None = None  # PR number for review/rework sessions
    rework_cycle: int | None = None  # Which rework iteration (1, 2, ...) — None for initial coding
    started_at: datetime = field(default_factory=datetime.now)
    status: SessionStatus = SessionStatus.RUNNING
    exit_sent: bool = False  # Track if we've already sent /exit
    completion_detected_at: datetime | None = None  # First time observer saw a valid completion.json for this session
    validation_retry_count: int = 0  # Number of validation retries attempted
    original_prompt: str | None = None  # Original prompt for retry context
    last_validation_error: str | None = None  # Last validation error for retry prompt
    last_validation_error_file: str | None = None  # Path to validation error file
    last_output_monotonic: float | None = None
    last_output_at: float | None = None
    last_log_mtime: float | None = None
    last_log_size: int | None = None
    last_output_tail: str | None = None
    last_no_output_monotonic: float | None = None
    # Claim/lease fields for multi-orchestrator coordination
    lease_id: str | None = None  # Unique identifier for the claim
    lease_acquired_at: datetime | None = None  # When the claim was acquired
    lease_expires_at: datetime | None = None  # When the claim expires if not renewed
    last_claim_verified_at: datetime | None = None  # Last time we verified we're still the winner
    # True for a tech_lead failure investigation running in a disposable, run-scoped
    # scratch worktree (#6823). The worktree is a throwaway that reads its focus
    # issue as evidence, so completion must remove it regardless of the cleanup
    # config (a "keep worktrees for inspection" preference is for PR-producing
    # coding worktrees, not for scratch investigation workspaces).
    scratch_worktree: bool = False

    def __post_init__(self) -> None:
        _require_session_run_assets(self.run_assets)

    @property
    def run_dir(self) -> Path:
        """Exact session artifact directory allocated before launch."""
        return self.run_assets.run_dir

    @property
    def runtime_minutes(self) -> int:
        """How long this session has been running."""
        delta = datetime.now() - self.started_at
        return int(delta.total_seconds() / 60)

    @property
    def is_timed_out(self) -> bool:
        """Check if session exceeded timeout."""
        return self.runtime_minutes > self.agent_config.timeout_minutes


def _require_session_run_assets(value: object) -> None:
    if isinstance(value, Mock) or not isinstance(value, SessionRunAssets):
        raise TypeError("Session.run_assets must be a SessionRunAssets")


def is_retrospective_review_session(session: Session) -> bool:
    """Return true for current and restored retrospective review sessions."""

    return (
        session.key.task == TaskKind.RETROSPECTIVE_REVIEW
        or session.terminal_id.startswith(RETROSPECTIVE_REVIEW_TERMINAL_PREFIX)
    )


def active_retrospective_review_issue_numbers(sessions: Iterable[Session]) -> set[int]:
    """Return issue numbers with an active retrospective review session."""

    return {
        session.issue.number
        for session in sessions
        if is_retrospective_review_session(session)
    }


