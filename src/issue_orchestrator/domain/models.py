@dataclass
class CompletionRecord:
    """Structured completion record written by agent-done.

    This is the ONLY output from agent-done - a JSON file that the orchestrator
    reads, validates (as untrusted input), and acts upon via adapters.

    The agent does NOT:
    - Push code
    - Create PRs
    - Post comments
    - Mutate labels

    The orchestrator verifies worktree state and executes all side effects.
    """
    # Core identification
    session_id: str  # Orchestrator's session identifier (e.g., "session-42")
    timestamp: str  # ISO format timestamp when agent-done was called

    # Outcome - what happened (not what to do)
    outcome: CompletionOutcome

    # Human-readable summary for logging/debugging
    summary: str

    # Requested actions - what the agent wants the orchestrator to do
    # Orchestrator validates and may reject/modify based on policy
    requested_actions: list[RequestedAction] = field(default_factory=list)

    # Status-specific data (all optional, validated based on outcome)
    # For COMPLETED
    implementation: Optional[str] = None  # What was implemented
    problems: Optional[str] = None  # Problems encountered

    # For BLOCKED
    blocked_reason: Optional[str] = None
    blocked_by: Optional[list[int]] = None  # Issue numbers
    attempted: Optional[str] = None
    when_unblocked: Optional[str] = None  # Hint for future agent

    # For NEEDS_HUMAN
    question: Optional[str] = None
    context: Optional[str] = None
    options: Optional[list[str]] = None
    default_action: Optional[str] = None

    # For REVIEW_* outcomes
    review_summary: Optional[str] = None  # For approved
    review_issues: Optional[str] = None  # For changes_requested
    risk_level: Optional[str] = None  # "low", "medium", "high"
    checks_passed: Optional[list[str]] = None
    checks_needed: Optional[list[str]] = None

    # Comment body to post (if POST_COMMENT in requested_actions)
    comment_body: Optional[str] = None

    # Extra labels to add to the PR (if CREATE_PR in requested_actions)
    pr_labels: Optional[list[str]] = None

    # Validation reference (if agent_gate was run)
    validation_record_path: Optional[str] = None  # Path to validation record JSON

    def to_dict(self) -> dict[str, Any]:
        """Convert to dictionary for JSON serialization."""
        return {
            "session_id": self.session_id,
            "timestamp": self.timestamp,
            "outcome": self.outcome.value,
            "summary": self.summary,
            "requested_actions": [a.value for a in self.requested_actions],
            "implementation": self.implementation,
            "problems": self.problems,
            "blocked_reason": self.blocked_reason,
            "blocked_by": self.blocked_by,
            "attempted": self.attempted,
            "when_unblocked": self.when_unblocked,
            "question": self.question,
            "context": self.context,
            "options": self.options,
            "default_action": self.default_action,
            "review_summary": self.review_summary,
            "review_issues": self.review_issues,
            "risk_level": self.risk_level,
            "checks_passed": self.checks_passed,
            "checks_needed": self.checks_needed,
            "comment_body": self.comment_body,
            "pr_labels": self.pr_labels,
            "validation_record_path": self.validation_record_path,
        }

    @classmethod
    def from_dict(cls, data: dict[str, Any]) -> "CompletionRecord":
        """Create from dictionary (JSON deserialization).

        Validates strictly - missing required fields or invalid values raise ValueError.
        This is UNTRUSTED input from the agent.
        """
        # Required fields
        required = ["session_id", "timestamp", "outcome", "summary"]
        for field_name in required:
            if field_name not in data:
                raise ValueError(f"Missing required field: {field_name}")

        try:
            outcome = CompletionOutcome(data["outcome"])
        except ValueError:
            raise ValueError(f"Invalid outcome: {data['outcome']}")

        requested_actions = []
        for action in data.get("requested_actions", []):
            try:
                requested_actions.append(RequestedAction(action))
            except ValueError:
                raise ValueError(f"Invalid requested action: {action}")

        return cls(
            session_id=data["session_id"],
            timestamp=data["timestamp"],
            outcome=outcome,
            summary=data["summary"],
            requested_actions=requested_actions,
            implementation=data.get("implementation"),
            problems=data.get("problems"),
            blocked_reason=data.get("blocked_reason"),
            blocked_by=data.get("blocked_by"),
            attempted=data.get("attempted"),
            when_unblocked=data.get("when_unblocked"),
            question=data.get("question"),
            context=data.get("context"),
            options=data.get("options"),
            default_action=data.get("default_action"),
            review_summary=data.get("review_summary"),
            review_issues=data.get("review_issues"),
            risk_level=data.get("risk_level"),
            checks_passed=data.get("checks_passed"),
            checks_needed=data.get("checks_needed"),
            comment_body=data.get("comment_body"),
            pr_labels=data.get("pr_labels"),
            validation_record_path=data.get("validation_record_path"),
        )


