"""OS-level proof that the ADR-0034 provider sandbox translations hold.

This is the integration counterpart to the pure-JSON unit tests in
``tests/unit/test_sandbox_provider_adapter.py``. Those assert the *shape* of the
``--settings`` payload; this test launches a **real** ``claude`` non-interactively
with the **generated** sandbox argv in a temp worktree and proves the operating
system actually enforces the boundary, by running one Bash command through the
agent and inspecting its side effects on disk.

The OS sandbox binds only Bash and its children; Claude's built-in Read/Edit/
Grep tools are governed by the PERMISSION layer instead. So this test exercises
BOTH boundaries — a sandboxed Bash ``cat`` (OS ``credentials.files``) and the
native ``Read`` tool (``permissions.deny``) — on the same planted secret.

What it proves (all via deterministic on-disk side effects, not model prose):

1. **Positive control** — a write *inside* the worktree succeeds, which proves
   the agent really executed sandboxed Bash (so the negatives below are real
   denials, not "the agent never ran the command").
2. **Write escape denied** — a write to a path *outside* the worktree never
   lands on disk (writes are cwd-bounded).
3. **Secret read denied — Bash / OS layer** — a secret planted at a
   ``credentials.files``-denied path is unreadable by a sandboxed ``cat``. The
   secret lives OUTSIDE the home dir (``/var/folders`` or ``/tmp``), where the
   default read policy is OPEN and ``denyRead: ["~/"]`` does NOT reach — so this
   isolates and proves the ``credentials.files`` deny.
4. **Network egress denied** — a raw TCP connect to a non-allowed host does not
   open (only ``api.anthropic.com`` is pre-allowed for Bash under model-only).
5. **Secret read denied — native ``Read`` tool** — the SAME secret is unreadable
   via the agent's built-in ``Read`` tool, proving the ``permissions.deny``
   native-tool layer (the direct fix for the review's follow-up: the OS sandbox
   does not cover native file tools). A native ``Read`` of a worktree file is the
   positive control that reads are not simply broken — the deny is secret-scoped.
6. **Anti-self-modification, Bash layer** — a Bash write to the agent's own
   policy file (``<worktree>/.claude/settings.json``, planted with a marker) is
   ``denyWrite``-blocked; the marker survives.
7. **Native write INSIDE the worktree SUCCEEDS** — a dedicated native ``Write``
   call lands the exact content, and stream-json shows the ``Write`` tool_use
   succeeded. This is the positive control that ``Edit(//worktree/**)`` governs
   the native file-editing tools.
8. **Native write OUTSIDE the worktree is DENIED** — from structured
   ``--output-format stream-json``: the ``Write`` tool_use occurs and its result
   is an error ("Permission to use Write has been denied"), and nothing lands.
9. **Native write to the policy file is DENIED** — a legitimate-looking,
   authorized config edit makes the agent actually call ``Write`` on its own
   policy file, and the SANDBOX (not the model's own judgement) denies it: the
   structured result is a *permission* denial (``is_error`` with a "denied by your
   permission settings" message) and the planted marker survives. Framing it as a
   real edit rather than an adversarial "tamper" is what forces the write attempt —
   an adversarial prompt triggers the model's own refusal, emits no tool_use, and
   would make the deny check pass vacuously. Together this establishes the
   agent-facing boundary under the trusted-repository contract (ADR-0034); a
   hostile *pre-existing* repo config is trusted policy, not tested here (the
   separate untrusted track, #6861).

Probes 7–9 are SEPARATE ``claude`` interactions so one model decision or denial
cannot suppress the others and cause a false pass.

The Codex test uses the same standard: a real ``codex exec --json`` interaction
must emit a completed command event and land an in-worktree positive-control
write. Its fixture is a real linked Git worktree, and the positive control must
also stage and commit the edit on that worktree's current branch. That same
command then proves an outside write, planted-secret read, raw TCP connection,
``.codex/config.toml`` self-modification, shared Git config modification, and
base-branch ref modification are denied.
Codex's invocation-scoped permission profile is exercised as generated by the
production provider; no test-only sandbox wrapper is involved.

Skip conditions:
- ``claude`` is not on PATH (mirrors the other live-agent gating).
- native Windows (the sandbox is macOS/Linux/WSL2 only).
- the sandbox cannot initialize on this host (e.g. bubblewrap missing on Linux):
  with ``failIfUnavailable: true`` claude fails to start, which we detect via
  the positive-control write and skip rather than report a false failure.
"""

