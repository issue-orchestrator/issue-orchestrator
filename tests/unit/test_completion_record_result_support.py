def _record(
    *,
    requested_actions: list[RequestedAction] | None = None,
    implementation: str | None = "Implemented the feature",
    problems: str | None = "None",
) -> CompletionRecord:
    return CompletionRecord(
        session_id="session-1",
        timestamp="2026-04-17T00:00:00+00:00",
        outcome=CompletionOutcome.COMPLETED,
        summary="done",
        requested_actions=requested_actions or [RequestedAction.CREATE_PR],
        implementation=implementation,
        problems=problems,
    )


class _NeverDeferringFinalizationOwner:
    """Owner stub for observer construction; always finalizes (no deferral)."""

    def completion_finalization_plan(
        self, **_kwargs: object
    ) -> CompletionFinalizationPlan:
        return CompletionFinalizationPlan(
            decision=CompletionFinalizationDecision.PROCESS,
            reason="stub owner always processes",
        )

    def cancel_deferred_review_exchange(self, **_kwargs: object) -> str | None:
        return None


class FakeGitAdapter:
    def __init__(
        self,
        *,
        branch: str | None = "123-feature",
        has_tracked_changes: bool = False,
    ) -> None:
        self.branch = branch
        self._has_tracked_changes = has_tracked_changes

    def get_current_branch(self, worktree: Path) -> str | None:
        return self.branch

    def has_uncommitted_changes(self, worktree: Path) -> bool:
        return self._has_tracked_changes

    def has_tracked_changes(self, worktree: Path, include_staged: bool = True) -> bool:
        return self._has_tracked_changes

    def list_dirty_files(self, worktree: Path, mode: str) -> list[str]:
        return ["src/app.py"] if self._has_tracked_changes else []


def test_build_pr_body_includes_issue_summary_and_footer() -> None:
    body = build_pr_body(_record(), issue_number=123)

    assert body == (
        "Closes #123\n"
        "\n"
        "## Implementation\n"
        "Implemented the feature\n"
        "\n"
        "## Problems Encountered\n"
        "None\n"
        "\n"
        "---\n"
        "*Generated by issue-orchestrator*"
    )


def test_build_pr_body_omits_empty_optional_sections() -> None:
    body = build_pr_body(
        _record(implementation=None, problems=None),
        issue_number=123,
    )

    assert "## Implementation" not in body
    assert "## Problems Encountered" not in body
    assert body.startswith("Closes #123")


def test_build_pr_body_includes_runtime_identity_audit_when_supplied() -> None:
    identity = RuntimeIdentity(
        package_version="1.2.3",
        source_commit_sha="abcdef1234567890abcdef1234567890abcdef12",
    )

    body = build_pr_body(_record(), issue_number=123, runtime_identity=identity)

    assert "## Orchestration Audit" in body
    assert "| Orchestrator version | `1.2.3` |" in body
    assert "| Orchestrator commit | `abcdef1234567890abcdef1234567890abcdef12` |" in body


def test_build_pr_body_runtime_identity_audit_handles_unknown_commit() -> None:
    identity = RuntimeIdentity(package_version="1.2.3")

    body = build_pr_body(_record(), issue_number=123, runtime_identity=identity)

    assert "| Orchestrator version | `1.2.3` |" in body
    assert "| Orchestrator commit | `unknown` |" in body


def test_validate_worktree_state_rejects_protected_push_branch(tmp_path: Path) -> None:
    validator = CompletionRecordValidator(
        config=Config(),
        git_adapter=FakeGitAdapter(branch="main"),
    )

    result = validator.validate_worktree_state(
        tmp_path,
        _record(requested_actions=[RequestedAction.PUSH_BRANCH]),
    )

    assert not result.ok
    assert result.failure == WorktreeValidationFailure.PROTECTED_BRANCH
    assert result.reason == "Cannot push: on protected branch 'main'"


def test_validate_worktree_state_applies_dirty_policy(tmp_path: Path) -> None:
    config = Config()
    config.validation.publish.dirty_check = "tracked"
    validator = CompletionRecordValidator(
        config=config,
        git_adapter=FakeGitAdapter(has_tracked_changes=True),
    )

    result = validator.validate_worktree_state(
        tmp_path,
        _record(requested_actions=[RequestedAction.PUSH_BRANCH]),
    )

    assert not result.ok
    assert result.failure == WorktreeValidationFailure.DIRTY_POLICY
    assert "src/app.py" in result.reason
    assert result.blocking_paths == ("src/app.py",)


def test_validate_worktree_state_allows_non_push_actions_on_main(tmp_path: Path) -> None:
    validator = CompletionRecordValidator(
        config=Config(),
        git_adapter=FakeGitAdapter(branch="main"),
    )

    result = validator.validate_worktree_state(
        tmp_path,
        _record(requested_actions=[RequestedAction.CREATE_PR]),
    )

    assert result.ok
    assert result.reason == ""
    assert result.failure is None


